package com.lazarin.practicing.leetcode.google.frequentquestions;

public class Event {

    private int start;
    private int end;

    public Event(int start, int end){
        this.start = start;
        this.end = end;
    }

    public int getStart(){
        return this.start;
    }

    public int getEnd(){
        return this.end;
    }
}
